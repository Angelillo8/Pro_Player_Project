class Team:

  def  __init__ (self, name, link, att, defe, mid, ovr, badge, league, id = None):
    self.name = name
    self.link = link
    self.att = att
    self.defe = defe
    self.mid = mid
    self.ovr = ovr
    self.badge = badge
    self.goals = 0
    self.wins = 0
    self.loses = 0
    self.draws = 0
    self.points = 0
    self.league = league
    self.id = id
  
    
